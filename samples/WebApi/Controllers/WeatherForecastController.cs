using Microsoft.AspNetCore.Mvc;

namespace WebApi.Controllers;

[ApiController]
[Route("[controller]")]
public class WeatherForecastController : ControllerBase
{
    private static readonly string[] Summaries = new[]
    {
        "Freezing", "Bracing", "Chilly", "Cool", "Mild", "Warm", "Balmy", "Hot", "Sweltering", "Scorching"
    };

    private readonly ILogger<WeatherForecastController> _logger;

    public WeatherForecastController(ILogger<WeatherForecastController> logger)
    {
        _logger = logger;
    }

    [HttpGet]
    public IEnumerable<WeatherForecast> Get()
    {
        return Enumerable.Range(1, 5).Select(index => new WeatherForecast
        {
            Date = DateOnly.FromDateTime(DateTime.Now.AddDays(index)),
            TemperatureC = Random.Shared.Next(-20, 55),
            Summary = Summaries[Random.Shared.Next(Summaries.Length)]
        })
        .ToArray();
    }

    [HttpGet("fast")]
    public IActionResult GetFast()
    {
        return Ok(new { message = "This is a fast endpoint", timestamp = DateTime.UtcNow });
    }

    [HttpGet("slow")]
    public async Task<IActionResult> GetSlow()
    {
        // Simulate slow operation
        await Task.Delay(1000);
        return Ok(new { message = "This is a slow endpoint", timestamp = DateTime.UtcNow });
    }

    [HttpGet("burst")]
    public IActionResult GetBurst()
    {
        return Ok(new { message = "This endpoint allows bursts", timestamp = DateTime.UtcNow });
    }
}

public class WeatherForecast
{
    public DateOnly Date { get; set; }
    public int TemperatureC { get; set; }
    public int TemperatureF => 32 + (int)(TemperatureC / 0.5556);
    public string? Summary { get; set; }
}
